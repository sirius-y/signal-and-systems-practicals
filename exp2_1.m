n=-5:0.1:5;
sig=2;
x1=exp(sig*n);
x2=n.*n;
x=x1+x2;
y=x1-x2;
z=x1.*x2;
k=x1./x2;
subplot(321);
stem(n,x1)
title('x1(n)');
subplot(322);
stem(n,x2)
title('x2(n)');
subplot(323);
stem(n,x)
title('addition');
subplot(324);
stem(n,y)
title('substraction');
subplot(325);
stem(n,z)
title('multiplication');
subplot(326);
stem(n,k)
title('division');

